/* Здесь в теории строковое значение должно было привестись к целочисленному и стать '0', но этого не произошло,
т.к. в новых версиях пхп приведение типов становится блоее строгим, чем в ранних версиях. поэтому мы получили 'false'
*/
var_dump(0 == 'hello, world');
/*
