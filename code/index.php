<?php
$a = 5;
$b = '05';
/* данный код выведет true, т.к. пхп имеет нестрогую типизацию, и в момент сравнения значение переменной b
стало из строкового целочисленным, то бишь '5'
*/
var_dump($a == $b);

// здесь также произойдет преобразование строкового значение в целочисленное, ноль соответственно исчезнет. итог '12345'
var_dump((int)'012345');

/* Здесь происходит строгое сравнение, соответсвенно сравниваются не только значения, но и типы данных, поэтому
 мы получим 'false'
*/
